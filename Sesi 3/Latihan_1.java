public class Latihan_1 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.print("* "); // munculkan bintang
        }

        for (int i = 0; i < 10; i++) {
            System.out.print(i + 1 + " ");
        }

        System.out.println();

        for (int i = 0; i < 20; i++) {
            if (i % 2 != 0) {
                System.out.print(i + " ");
            }
        }
    }
}
